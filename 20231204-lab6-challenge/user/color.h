/* Define color modes */
#define BOLD_GREEN(word) "\033[0;32;32m\033[1m" # word "\033[m"
#define RED(word) "\033[0;32;31m" # word "\033[m"
#define LIGHT_RED(word) "\033[1;31m" # word "\033[m"
#define GREEN(word) "\033[0;32;32m" # word "\033[m"
#define LIGHT_GREEN(word) "\033[1;32m" # word "\033[m"
#define BLUE(word) "\033[0;32;34m" # word "\033[m"
#define LIGHT_BLUE(word) "\033[1;34m" # word "\033[m"
#define DARK_GRAY(word) "\033[1;30m" # word "\033[m"
#define CYAN(word) "\033[0;36m" # word "\033[m"
#define LIGHT_CYAN(word) "\033[1;36m" # word "\033[m"
#define PURPLE(word) "\033[0;35m" # word "\033[m"
#define LIGHT_PURPLE(word) "\033[1;35m" # word "\033[m"
#define BROWN(word) "\033[0;33m" # word "\033[m"
#define YELLOW(word) "\033[1;33m" # word "\033[m"
#define LIGHT_GRAY(word) "\033[0;37m" # word "\033[m"
#define WHITE(word) "\033[1;37m" # word "\033[m"
